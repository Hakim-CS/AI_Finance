"""
train_model_v2.py
=============================================================================
Trains a Random Forest model to predict monthly category spending.

INPUT  : monthly_summary.csv (generated by generate_training_data.py)
OUTPUT : budget_model_v2.pkl          (the trained model)
         evaluation_results.json      (MAE, RMSE, R2 metrics)
         charts/                      (PNG charts for thesis)

HOW IT WORKS (step by step):
=============================================================================
1. Load the monthly summary (180 rows: 5 users x 36 months)
2. Create "previous month" features (what did the user spend LAST month?)
3. Split into training set (years 2023-2024) and test set (year 2025)
4. Train a Random Forest model for EACH category independently
5. Evaluate against two baselines:
   - "Last Month" baseline: predict this month = last month
   - "Average" baseline:    predict this month = user's average
6. Save everything: model, metrics, charts

Author : Aura Finance Thesis Project
Date   : April 2026
=============================================================================
"""

import os
import json
import joblib
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# Try to import matplotlib; if not installed, charts will be skipped
try:
    import matplotlib
    matplotlib.use('Agg')  # Non-interactive backend (no GUI needed)
    import matplotlib.pyplot as plt
    HAS_MATPLOTLIB = True
except ImportError:
    HAS_MATPLOTLIB = False
    print("[WARNING] matplotlib not installed. Skipping chart generation.")
    print("  Install with: pip install matplotlib")

# =============================================================================
# CONFIGURATION
# =============================================================================
CATEGORIES = ["food", "transport", "shopping", "entertainment",
              "utilities", "health", "travel", "other"]

AI_DIR     = os.path.dirname(os.path.abspath(__file__))
CHARTS_DIR = os.path.join(AI_DIR, "charts")

# =============================================================================
# STEP 1: Load and Prepare Data
# =============================================================================
def load_data() -> pd.DataFrame:
    """
    Load monthly_summary.csv and add "previous month" features.

    WHY previous-month features?
    ----------------------------
    The key insight in financial prediction is:
        "The best predictor of what you'll spend next month is what you spent this month."

    By adding last month's spending as input features, we give the model
    something meaningful to learn from. Without these, the model only knows
    income and month number -- which is not enough to capture personal habits.

    BEFORE (what the old model had):
        income -> [MODEL] -> food, transport, ...

    AFTER (what our new model has):
        income + month + prev_food + prev_transport + ... -> [MODEL] -> food, transport, ...
    """
    path = os.path.join(AI_DIR, "monthly_summary.csv")
    df = pd.read_csv(path)

    print(f"  Loaded {len(df)} monthly records from {path}")
    print(f"  Users: {sorted(df['user_id'].unique())}")
    print(f"  Months: {sorted(df['month'].unique())}")
    print()

    # Sort by user_id, year, and month to ensure correct "previous month" alignment
    df = df.sort_values(["user_id", "year", "month"]).reset_index(drop=True)

    # Add "previous month" features for each category
    # shift(1) moves each row's value DOWN by 1, so row N gets row N-1's value
    for cat in CATEGORIES:
        df[f"prev_{cat}"] = df.groupby("user_id")[cat].shift(1)

    # Add previous month's total spending
    df["prev_total"] = df.groupby("user_id")["total_spent"].shift(1)

    # Drop month 1 for each user (no "previous month" data available)
    # This is expected: we lose the first month per user (5 rows) = 55 rows remain
    df = df.dropna().reset_index(drop=True)

    print(f"  After adding previous-month features: {len(df)} rows")
    print(f"  Feature columns: income, month, prev_food, prev_transport, ..., prev_total")
    print()

    return df


# =============================================================================
# STEP 2: Define Features and Targets
# =============================================================================
def prepare_features(df: pd.DataFrame):
    """
    Split data into:
      X (features): what the model sees as INPUT
      y (targets):  what the model tries to PREDICT

    Features we use:
      - income         : user's monthly income
      - month          : month number (1-12), captures seasonality
      - prev_food      : how much the user spent on food LAST month
      - prev_transport : how much the user spent on transport LAST month
      - ... (one per category)
      - prev_total     : total spending last month

    Target:
      - food, transport, ... (this month's actual spending per category)
    """
    feature_cols = ["income", "month"] + [f"prev_{cat}" for cat in CATEGORIES] + ["prev_total"]
    target_cols  = CATEGORIES

    X = df[feature_cols]
    y = df[target_cols]

    return X, y, feature_cols, target_cols


# =============================================================================
# STEP 3: Train/Test Split
# =============================================================================
def split_data(df: pd.DataFrame, X: pd.DataFrame, y: pd.DataFrame):
    """
    Split into training and test sets using a TIME-BASED split.

    WHY time-based instead of random?
    ---------------------------------
    In financial forecasting, you train on PAST data and predict the FUTURE.
    A random split would let the model "peek" at future months, which is
    cheating. Time-based split is more realistic and academically honest.

    Training: Years 2023-2024 (24 months per user)
    Testing:  Year 2025 (12 months per user)
    """
    train_mask = df["year"] <= 2024
    test_mask  = df["year"] == 2025

    X_train = X[train_mask]
    X_test  = X[test_mask]
    y_train = y[train_mask]
    y_test  = y[test_mask]

    print(f"  Training set: {len(X_train)} rows (years 2023-2024)")
    print(f"  Test set:     {len(X_test)} rows (year 2025)")
    print()

    return X_train, X_test, y_train, y_test, train_mask, test_mask


# =============================================================================
# STEP 4: Train the Model
# =============================================================================
def train_model(X_train, y_train, feature_cols):
    """
    Train one Random Forest model per category.

    WHY one model per category?
    ---------------------------
    Each spending category has different patterns:
      - "utilities" is very stable month-to-month
      - "travel" is very spiky (0 most months, high in summer)
      - "food" is moderately consistent

    Training separate models lets each one specialize. This is called
    "multi-target regression" and it's better than forcing one model to
    predict everything at once.

    WHY Random Forest?
    ------------------
    1. Works great with small datasets (we have ~45 training rows)
    2. Handles non-linear relationships (e.g., seasonality)
    3. Provides feature importance (great for thesis)
    4. Less prone to overfitting than neural networks on small data
    5. No need for feature normalization
    """
    models = {}
    feature_importances = {}

    for cat in CATEGORIES:
        rf = RandomForestRegressor(
            n_estimators=100,     # 100 decision trees
            max_depth=6,          # Limit depth to prevent overfitting
            min_samples_leaf=3,   # Each leaf must have at least 3 samples
            random_state=42,      # Reproducible results
        )
        rf.fit(X_train, y_train[cat])
        models[cat] = rf

        # Extract feature importance (for thesis)
        importance = dict(zip(feature_cols, rf.feature_importances_))
        feature_importances[cat] = importance

        # Print top 3 most important features per category
        sorted_imp = sorted(importance.items(), key=lambda x: x[1], reverse=True)
        top3 = ", ".join(f"{k}: {v:.2f}" for k, v in sorted_imp[:3])
        print(f"    {cat:>15}: top features = {top3}")

    return models, feature_importances


# =============================================================================
# STEP 5: Evaluate
# =============================================================================
def evaluate(models, X_test, y_test, df, test_mask):
    """
    Compare our model against two baselines.

    BASELINE 1: "Last Month"
    ========================
    Predict this month's spending = exactly what was spent last month.
    This is the simplest possible prediction and is surprisingly hard to beat.

    BASELINE 2: "User Average"
    ==========================
    Predict this month's spending = user's average spending over all months.
    This captures the general level but misses trends and seasonality.

    OUR MODEL: Random Forest
    ========================
    Uses income + month + previous spending to make informed predictions.

    METRICS:
    --------
    MAE  = Mean Absolute Error   : Average $ difference (lower is better)
    RMSE = Root Mean Squared Error: Like MAE but penalizes big errors more
    R2   = R-squared             : 1.0 = perfect, 0.0 = useless, <0 = worse than average
    """
    results = {}

    for cat in CATEGORIES:
        actual     = y_test[cat].values
        predicted  = models[cat].predict(X_test)

        # Baseline 1: last month = prev_{category}
        baseline_last = X_test[f"prev_{cat}"].values

        # Baseline 2: user average (calculate from training data)
        test_df = df[test_mask]
        user_avgs = df.groupby("user_id")[cat].mean()
        baseline_avg = test_df["user_id"].map(user_avgs).values

        results[cat] = {
            "our_model": {
                "MAE":  round(mean_absolute_error(actual, predicted), 2),
                "RMSE": round(np.sqrt(mean_squared_error(actual, predicted)), 2),
                "R2":   round(r2_score(actual, predicted), 4),
            },
            "last_month_baseline": {
                "MAE":  round(mean_absolute_error(actual, baseline_last), 2),
                "RMSE": round(np.sqrt(mean_squared_error(actual, baseline_last)), 2),
                "R2":   round(r2_score(actual, baseline_last), 4),
            },
            "average_baseline": {
                "MAE":  round(mean_absolute_error(actual, baseline_avg), 2),
                "RMSE": round(np.sqrt(mean_squared_error(actual, baseline_avg)), 2),
                "R2":   round(r2_score(actual, baseline_avg), 4),
            },
            "actual_values":    [round(v, 2) for v in actual.tolist()],
            "predicted_values": [round(v, 2) for v in predicted.tolist()],
        }

    # Calculate overall (average across all categories)
    overall = {"our_model": {}, "last_month_baseline": {}, "average_baseline": {}}
    for method in overall:
        for metric in ["MAE", "RMSE", "R2"]:
            vals = [results[cat][method][metric] for cat in CATEGORIES]
            overall[method][metric] = round(sum(vals) / len(vals), 2)

    results["_overall"] = overall

    return results


