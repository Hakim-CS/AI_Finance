# STEP 6: Generate Charts
# =============================================================================
def generate_charts(results, models, feature_cols, X_test, y_test):
    """Generate publication-quality charts for the thesis."""
    if not HAS_MATPLOTLIB:
        print("  [SKIP] Charts skipped (matplotlib not installed)")
        return

    os.makedirs(CHARTS_DIR, exist_ok=True)

    # Set a clean style
    plt.style.use('seaborn-v0_8-whitegrid')
    plt.rcParams.update({'font.size': 11, 'figure.dpi': 150})

    # --- Chart 1: MAE Comparison Bar Chart ---
    fig, ax = plt.subplots(figsize=(12, 6))
    x = np.arange(len(CATEGORIES))
    width = 0.25

    mae_ours = [results[c]["our_model"]["MAE"] for c in CATEGORIES]
    mae_last = [results[c]["last_month_baseline"]["MAE"] for c in CATEGORIES]
    mae_avg  = [results[c]["average_baseline"]["MAE"] for c in CATEGORIES]

    bars1 = ax.bar(x - width, mae_last, width, label="Last Month Baseline", color="#94a3b8", edgecolor="white")
    bars2 = ax.bar(x,         mae_avg,  width, label="Average Baseline",    color="#f59e0b", edgecolor="white")
    bars3 = ax.bar(x + width, mae_ours, width, label="Our Random Forest",   color="#10b981", edgecolor="white")

    ax.set_xlabel("Spending Category", fontweight="bold")
    ax.set_ylabel("Mean Absolute Error ($)", fontweight="bold")
    ax.set_title("Model Performance: MAE Comparison by Category", fontweight="bold", fontsize=14)
    ax.set_xticks(x)
    ax.set_xticklabels([c.capitalize() for c in CATEGORIES], rotation=25, ha="right")
    ax.legend(loc="upper right")
    ax.grid(axis="y", alpha=0.3)

    plt.tight_layout()
    plt.savefig(os.path.join(CHARTS_DIR, "mae_comparison.png"), bbox_inches="tight")
    plt.close()
    print("    [OK] charts/mae_comparison.png")

    # --- Chart 2: Predicted vs Actual Scatter ---
    fig, axes = plt.subplots(2, 4, figsize=(16, 8))
    axes = axes.flatten()

    for i, cat in enumerate(CATEGORIES):
        ax = axes[i]
        actual = results[cat]["actual_values"]
        predicted = results[cat]["predicted_values"]

        ax.scatter(actual, predicted, alpha=0.7, color="#10b981", edgecolors="white", s=60)

        # Perfect prediction line
        all_vals = actual + predicted
        min_val = min(all_vals) * 0.8
        max_val = max(all_vals) * 1.2
        ax.plot([min_val, max_val], [min_val, max_val], "r--", alpha=0.5, label="Perfect prediction")

        ax.set_xlabel("Actual ($)", fontsize=9)
        ax.set_ylabel("Predicted ($)", fontsize=9)
        ax.set_title(cat.capitalize(), fontweight="bold")
        ax.grid(alpha=0.3)

    fig.suptitle("Predicted vs Actual Spending by Category", fontweight="bold", fontsize=14, y=1.02)
    plt.tight_layout()
    plt.savefig(os.path.join(CHARTS_DIR, "predicted_vs_actual.png"), bbox_inches="tight")
    plt.close()
    print("    [OK] charts/predicted_vs_actual.png")

    # --- Chart 3: Feature Importance (averaged across all categories) ---
    fig, ax = plt.subplots(figsize=(10, 6))

    avg_importance = {}
    for feat in feature_cols:
        total = 0
        for cat in CATEGORIES:
            total += models[cat].feature_importances_[feature_cols.index(feat)]
        avg_importance[feat] = total / len(CATEGORIES)

    sorted_feats = sorted(avg_importance.items(), key=lambda x: x[1], reverse=True)
    feat_names = [f[0] for f in sorted_feats]
    feat_vals  = [f[1] for f in sorted_feats]

    colors = ["#10b981" if v > 0.05 else "#94a3b8" for v in feat_vals]
    ax.barh(range(len(feat_names)), feat_vals, color=colors, edgecolor="white")
    ax.set_yticks(range(len(feat_names)))
    ax.set_yticklabels([n.replace("prev_", "prev ") for n in feat_names])
    ax.invert_yaxis()
    ax.set_xlabel("Importance Score", fontweight="bold")
    ax.set_title("Feature Importance (Averaged Across All Categories)", fontweight="bold", fontsize=14)
    ax.grid(axis="x", alpha=0.3)

    plt.tight_layout()
    plt.savefig(os.path.join(CHARTS_DIR, "feature_importance.png"), bbox_inches="tight")
    plt.close()
    print("    [OK] charts/feature_importance.png")

    # --- Chart 4: Overall Summary Table as Image ---
    fig, ax = plt.subplots(figsize=(10, 4))
    ax.axis("off")

    table_data = [["Category", "Our Model MAE", "Last Month MAE", "Average MAE", "Improvement"]]
    for cat in CATEGORIES:
        ours = results[cat]["our_model"]["MAE"]
        last = results[cat]["last_month_baseline"]["MAE"]
        improvement = ((last - ours) / last * 100) if last > 0 else 0
        table_data.append([
            cat.capitalize(),
            f"${ours:,.2f}",
            f"${last:,.2f}",
            f"${results[cat]['average_baseline']['MAE']:,.2f}",
            f"{improvement:+.1f}%",
        ])

    # Overall row
    o = results["_overall"]
    imp = ((o["last_month_baseline"]["MAE"] - o["our_model"]["MAE"]) / o["last_month_baseline"]["MAE"] * 100) if o["last_month_baseline"]["MAE"] > 0 else 0
    table_data.append([
        "OVERALL",
        f"${o['our_model']['MAE']:,.2f}",
        f"${o['last_month_baseline']['MAE']:,.2f}",
        f"${o['average_baseline']['MAE']:,.2f}",
        f"{imp:+.1f}%",
    ])

    table = ax.table(cellText=table_data, loc="center", cellLoc="center")
    table.auto_set_font_size(False)
    table.set_fontsize(10)
    table.scale(1, 1.5)

    # Style header row
    for j in range(len(table_data[0])):
        table[(0, j)].set_facecolor("#1e293b")
        table[(0, j)].set_text_props(color="white", fontweight="bold")
    # Style overall row
    last_row = len(table_data) - 1
    for j in range(len(table_data[0])):
        table[(last_row, j)].set_facecolor("#ecfdf5")
        table[(last_row, j)].set_text_props(fontweight="bold")

    ax.set_title("Model Evaluation Summary", fontweight="bold", fontsize=14, pad=20)

    plt.tight_layout()
    plt.savefig(os.path.join(CHARTS_DIR, "evaluation_summary.png"), bbox_inches="tight")
    plt.close()
    print("    [OK] charts/evaluation_summary.png")


# =============================================================================
# MAIN
# =============================================================================
def main():
    print("=" * 70)
    print("  AURA FINANCE -- Model Training Pipeline (v2)")
    print("=" * 70)
    print()

    # Step 1: Load data
    print("[1/6] Loading data...")
    df = load_data()

    # Step 2: Prepare features
    print("[2/6] Preparing features...")
    X, y, feature_cols, target_cols = prepare_features(df)
    print(f"  Feature count: {len(feature_cols)}")
    print(f"  Target count:  {len(target_cols)}")
    print()

    # Step 3: Split
    print("[3/6] Splitting into train/test...")
    X_train, X_test, y_train, y_test, train_mask, test_mask = split_data(df, X, y)

    # Step 4: Train
    print("[4/6] Training Random Forest models...")
    models, feat_importances = train_model(X_train, y_train, feature_cols)
    print()

    # Step 5: Evaluate
    print("[5/6] Evaluating...")
    results = evaluate(models, X_test, y_test, df, test_mask)

    # Print comparison table
    print()
    print("  " + "-" * 66)
    print(f"  {'Category':<15} {'Our Model':>12} {'Last Month':>12} {'Average':>12} {'Improve':>10}")
    print("  " + "-" * 66)
    for cat in CATEGORIES:
        ours = results[cat]["our_model"]["MAE"]
        last = results[cat]["last_month_baseline"]["MAE"]
        avg  = results[cat]["average_baseline"]["MAE"]
        imp  = ((last - ours) / last * 100) if last > 0 else 0
        print(f"  {cat:<15} ${ours:>10,.2f} ${last:>10,.2f} ${avg:>10,.2f} {imp:>+9.1f}%")

    o = results["_overall"]
    imp = ((o["last_month_baseline"]["MAE"] - o["our_model"]["MAE"]) / o["last_month_baseline"]["MAE"] * 100) if o["last_month_baseline"]["MAE"] > 0 else 0
    print("  " + "-" * 66)
    print(f"  {'OVERALL':<15} ${o['our_model']['MAE']:>10,.2f} ${o['last_month_baseline']['MAE']:>10,.2f} ${o['average_baseline']['MAE']:>10,.2f} {imp:>+9.1f}%")
    print("  " + "-" * 66)
    print()

    # Step 6: Save
    print("[6/6] Saving model and results...")

    # Save the trained models
    model_path = os.path.join(AI_DIR, "budget_model_v2.pkl")
    joblib.dump({
        "models":        models,
        "feature_cols":  feature_cols,
        "target_cols":   target_cols,
        "categories":    CATEGORIES,
    }, model_path)
    print(f"    [OK] Model saved to: {model_path}")

    # Save evaluation results
    results_path = os.path.join(AI_DIR, "evaluation_results.json")
    # Remove numpy arrays for JSON serialization
    clean_results = {}
    for k, v in results.items():
        if isinstance(v, dict):
            clean_results[k] = {}
            for k2, v2 in v.items():
                if isinstance(v2, dict):
                    clean_results[k][k2] = {kk: float(vv) if isinstance(vv, (np.floating, float)) else vv for kk, vv in v2.items()}
                elif isinstance(v2, list):
                    clean_results[k][k2] = [float(x) for x in v2]
                else:
                    clean_results[k][k2] = v2

    with open(results_path, "w") as f:
        json.dump(clean_results, f, indent=2)
    print(f"    [OK] Results saved to: {results_path}")

    # Save feature importances
    fi_path = os.path.join(AI_DIR, "feature_importances.json")
    clean_fi = {}
    for cat, imp_dict in feat_importances.items():
        clean_fi[cat] = {k: round(float(v), 4) for k, v in imp_dict.items()}
    with open(fi_path, "w") as f:
        json.dump(clean_fi, f, indent=2)
    print(f"    [OK] Feature importances saved to: {fi_path}")

    # Generate charts
    print()
    print("  Generating charts...")
    generate_charts(results, models, feature_cols, X_test, y_test)

    print()
    print("=" * 70)
    print("  DONE! Your AI model is trained and evaluated.")
    print("  Next step: Update the backend to use budget_model_v2.pkl")
    print("=" * 70)


if __name__ == "__main__":
    main()
